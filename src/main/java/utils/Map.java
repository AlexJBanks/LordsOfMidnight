  /**
   * basic constructor that takes raw ints and performs preprocessing
   *
   * @param map_ 2d array of ints
   * @see this#getSpawnPoint()
   * @see ResourceLoader#loadMap(String)
   */
  public Map(int[][] map_) {
    MAP = map_;
    MAX_X = MAP.length;
    MAX_Y = MAP[0].length;
    SPAWN_POINT = loadSpawnPoint();
  }

  /**
   * called on construction to find and load spawn point
   * TODO: 30/1/19 get spawn int from MapColour
   *
   * @return Point2D.Double spawnPoint, null if none found.
   * @see this#Map(int[][])
   */
  private Double loadSpawnPoint() {
    for (int i = 0; i < MAX_X; i++) {
      for (int j = 0; j < MAX_Y; j++) {
        if (MAP[i][j] == 2) {
          return new Double(i, j);
        }
      }
    }
    return null;
  }
  
  public int getMaxX() {
	  return MAX_X;
  }
  
  public int getMaxY() {
	  return MAX_Y;
  }

  /**
   * calculates if point is out of bounds modular arithmetic for map looping
   * TODO: 30/1/19 get wall int from MapColour
   *
   * @param point location to be checked
   * @return true if wall
   */
  public boolean isWall(Double point) {
    return MAP[(int) (point.getX() % MAX_X)][(int) (point.getY() % MAX_Y)] == 1;
  }

  /**
   * package method for raw processing
   * @return 2D Array of ints
   */
  int[][] raw() {
    return MAP;
  }

  /**
   * @return location to position ghouls at if caught
   */
  public Double getSpawnPoint() {
    return SPAWN_POINT;
  }
